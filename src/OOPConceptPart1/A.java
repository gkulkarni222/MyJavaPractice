package OOPConceptPart1;

public class A {
	
	public A() {
		System.out.println("Parent class constructor");
	}
	
	public A(int i) {
		System.out.println("Parent constructor i value is "+i);
	}

	public A(int i,int j) {
		System.out.println("Parent constructor i value is "+i);
		System.out.println("Parent constructor j value is "+j);
	}

}
